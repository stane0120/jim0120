SELECT M2.INVOICE_NO, M2.LINE_NO, M2.ITEM_CD, M2.ORDER_QTY, M1.SET_QTY, M2.ITEM_NM, C1.ITEM_NM
FROM LO_OUT_M M1
     JOIN LO_OUT_D M2 ON M2.INVOICE_NO = M1.INVOICE_NO
     JOIN CM_ITEM C1 ON C1.ITEM_CD = M2.ITEM_CD
WHERE M1.OUTBOUND_DATE = '2019-06-03'
  AND M1.OUT_TYPE_DIV = 'M12'
  AND M1.OUT_BOX_DIV = 'D3'
  AND M1.OUTBOUND_BATCH = '003'
  AND M1.INVOICE_NO LIKE '346724703%'
  GROUP BY M2.ITEM_CD, C1.ITEM_NM
ORDER BY M2.INVOICE_NO, M2.LINE_NO;



